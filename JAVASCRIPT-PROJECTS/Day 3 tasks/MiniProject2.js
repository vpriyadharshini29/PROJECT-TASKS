let inputColor=prompt("Enter a color");
switch(inputColor){
    case "Red":
    console.log("Stop!");
    break;
    case "Yellow":
    console.log("Slow down!");
    break;
    case "Green":
    console.log("Go!");
    break;
    default:
        console.log("Invalid color!");
        
}